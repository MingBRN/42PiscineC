#include <stdio.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	tmp;

	i = 0;
	while (i < size)
	{
		j = 0;
		while (j < size)
		{
			if (tab[i] <= tab[j])
			{
				tmp = tab[i];
				tab[i] = tab[j];
				tab[j] = tmp;
			}
			j++;
		}
		i++;
	}
}

int	main(void)
{
	int a[] = {4, 5, 3};
	ft_sort_int_tab(a, 3);
	for (int i = 0; i < 3; i++)
	{
		printf("%d", a[i]);
	}
	
}