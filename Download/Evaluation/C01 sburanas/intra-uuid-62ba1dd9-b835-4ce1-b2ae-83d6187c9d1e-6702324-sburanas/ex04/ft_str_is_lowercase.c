#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	while (*str)
	{
		if (!(*str >= 'a' && *str <= 'z'))
		{
			return (0);
		}
		str++;
	}
	return (1);
}

int	main(void)
{
	char	c[] = "asdf";
	int i;

	i = ft_str_is_lowercase(c);
	printf("%d ", i);
}
