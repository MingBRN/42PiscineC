#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	while (*str)
	{
		if (!(*str >= 'A' && *str <= 'Z'))
		{
			return (0);
		}
		str++;
	}
	return (1);
}

int	main(void)
{
	char	c[] = "SDF";
	int	i;

	i = ft_str_is_uppercase(c);
	printf("%d ",i);
}
