#include <unistd.h>
#include <stdio.h>

char	*ft_strupcase(char *str)
{
	char	*src;

	src = str;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
	}
	return (src);
}

int	main(void)
{
	char	c[] = "hello world";
	

	
	printf("%s ",ft_strupcase(c));
}
